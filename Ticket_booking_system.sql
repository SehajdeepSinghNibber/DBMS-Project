CREATE OR REPLACE FUNCTION fn_is_seat_available(
    p_show_id IN NUMBER,
    p_seat_id IN NUMBER
) RETURN NUMBER IS
    v_count NUMBER;
BEGIN
    SELECT COUNT(*)
    INTO v_count
    FROM TICKETS t
    JOIN BOOKINGS b ON t.booking_id = b.booking_id
    WHERE t.show_id = p_show_id 
    AND t.seat_id = p_seat_id
    AND b.status = 'CONFIRMED';
    
    RETURN CASE WHEN v_count = 0 THEN 1 ELSE 0 END;
END;
/

CREATE OR REPLACE PROCEDURE pr_book_ticket(
    p_user_id IN NUMBER,
    p_show_id IN NUMBER,
    p_seat_id IN NUMBER
) IS
    v_base_price NUMBER(10, 2);
    v_booking_id NUMBER;
BEGIN
    IF fn_is_seat_available(p_show_id, p_seat_id) = 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'Seat is already booked or pending for this show.');
    END IF;

    SELECT base_price INTO v_base_price FROM SHOWS WHERE show_id = p_show_id;

    INSERT INTO BOOKINGS (user_id, show_id, total_amount, status)
    VALUES (p_user_id, p_show_id, v_base_price, 'CONFIRMED')
    RETURNING booking_id INTO v_booking_id;

    INSERT INTO TICKETS (booking_id, show_id, seat_id, actual_price)
    VALUES (v_booking_id, p_show_id, p_seat_id, v_base_price);

    DBMS_OUTPUT.PUT_LINE('Booking created. ID: ' || v_booking_id || '. Please COMMIT to finalize.');

EXCEPTION
    WHEN DUP_VAL_ON_INDEX THEN
        RAISE_APPLICATION_ERROR(-20002, 'RACE CONDITION DETECTED: This seat was just taken by another user!');
    WHEN OTHERS THEN
        DBMS_OUTPUT.PUT_LINE('Booking failed: ' || SQLERRM);
        RAISE;
END;
/

CREATE OR REPLACE TRIGGER trg_audit_booking
AFTER INSERT OR UPDATE ON BOOKINGS
FOR EACH ROW
DECLARE
    v_action VARCHAR2(20);
BEGIN
    IF INSERTING THEN
        v_action := 'INSERT';
    ELSE
        v_action := 'UPDATE';
    END IF;

    INSERT INTO AUDIT_LOGS (table_name, action, record_id, details)
    VALUES ('BOOKINGS', 
            v_action, 
            :NEW.booking_id,
            'User: ' || :NEW.user_id || ' | Show: ' || :NEW.show_id || ' | Status: ' || :NEW.status);
END;
/

CREATE OR REPLACE PROCEDURE pr_generate_revenue_report IS
    CURSOR c_revenue IS
        SELECT e.title, NVL(SUM(b.total_amount), 0) as total_revenue, COUNT(b.booking_id) as booking_count
        FROM EVENT e
        JOIN SHOWS s ON e.event_id = s.event_id
        LEFT JOIN BOOKINGS b ON s.show_id = b.show_id AND b.status = 'CONFIRMED'
        GROUP BY e.title;
BEGIN
    DBMS_OUTPUT.PUT_LINE(RPAD('-', 60, '-'));
    DBMS_OUTPUT.PUT_LINE(RPAD('EVENT TITLE', 30) || ' | ' || LPAD('BOOKINGS', 10) || ' | ' || LPAD('REVENUE', 12));
    DBMS_OUTPUT.PUT_LINE(RPAD('-', 60, '-'));
    
    FOR r IN c_revenue LOOP
        DBMS_OUTPUT.PUT_LINE(
            RPAD(r.title, 30) || ' | ' || 
            LPAD(r.booking_count, 10) || ' | ' || 
            LPAD(TO_CHAR(r.total_revenue, '$99,990.99'), 12)
        );
    END LOOP;
    DBMS_OUTPUT.PUT_LINE(RPAD('-', 60, '-'));
END;
/

