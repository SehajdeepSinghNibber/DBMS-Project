CREATE TABLE USERS (
    user_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR2(100) NOT NULL,
    email VARCHAR2(100) UNIQUE NOT NULL,
    phone VARCHAR2(20),
    password_hash VARCHAR2(255) NOT NULL,
    created_at DATE DEFAULT SYSDATE
);

CREATE TABLE EVENT (
    event_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    title VARCHAR2(200) NOT NULL,
    event_type VARCHAR2(20) CHECK (event_type IN ('MOVIE', 'CONCERT', 'STANDUP', 'SPORT', 'OTHER')),
    duration_minutes NUMBER,
    language VARCHAR2(50),
    description CLOB
);

CREATE TABLE VENUES (
    venue_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    venue_name VARCHAR2(100) NOT NULL,
    location VARCHAR2(200),
    total_capacity NUMBER NOT NULL
);

CREATE TABLE SHOWS (
    show_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_id NUMBER NOT NULL REFERENCES EVENT(event_id),
    venue_id NUMBER NOT NULL REFERENCES VENUES(venue_id),
    start_time TIMESTAMP NOT NULL,
    base_price NUMBER(10, 2) NOT NULL
);
CREATE TABLE SEATS (
    seat_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    venue_id NUMBER NOT NULL REFERENCES VENUES(venue_id),
    seat_number VARCHAR2(10) NOT NULL,
    row_label CHAR(1) NOT NULL,
    category VARCHAR2(20) CHECK (category IN ('PLATINUM', 'GOLD', 'SILVER')),
    CONSTRAINT UNIQUE_SEAT UNIQUE (venue_id, row_label, seat_number)
);

CREATE TABLE BOOKINGS (
    booking_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id NUMBER NOT NULL REFERENCES USERS(user_id),
    show_id NUMBER NOT NULL REFERENCES SHOWS(show_id),
    booking_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    total_amount NUMBER(10, 2) NOT NULL,
    status VARCHAR2(20) DEFAULT 'CONFIRMED' CHECK (status IN ('CONFIRMED', 'CANCELLED', 'PENDING'))
);

CREATE TABLE TICKETS (
    ticket_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    booking_id NUMBER NOT NULL REFERENCES BOOKINGS(booking_id) ON DELETE CASCADE,
    show_id NUMBER NOT NULL REFERENCES SHOWS(show_id),
    seat_id NUMBER NOT NULL REFERENCES SEATS(seat_id),
    actual_price NUMBER(10, 2) NOT NULL,
    CONSTRAINT UNIQUE_SHOW_SEAT UNIQUE (show_id, seat_id)
);

CREATE TABLE AUDIT_LOGS (
    audit_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    table_name VARCHAR2(30),
    action VARCHAR2(20),
    record_id NUMBER,
    action_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    details VARCHAR2(500)
);
